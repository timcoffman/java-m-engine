package edu.vanderbilt.clinicalsystems.m.lang.text;

public class RoutineWriterException extends Exception {
	private static final long serialVersionUID = 1L;

	public RoutineWriterException() { super(); }
	public RoutineWriterException(String message, Throwable cause) { super(message, cause); }
	public RoutineWriterException(String message) { super(message); }
	public RoutineWriterException(Throwable cause) { super(cause); }
}
